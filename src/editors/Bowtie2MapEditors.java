    private void setIDGCombobox (File dir) {
        BOWTIE2MAP_IDG_selected_ComboBox.removeAllItems();
        BOWTIE2MAP_IDG_selected_ComboBox.addItem("Choose_an_indexed_Genome");
        Hashtable<String,String> h = new Hashtable<String,String>();
        String s = "";
        for (File f:dir.listFiles()) {
            if (f.getName().contains(".bt2")){
                s = f.getName().replaceAll("\\.\\d.bt2","");
                s = s.replaceAll("\\.rev$","");
                if (!h.containsKey(s)){
                    h.put(s,"");
                    BOWTIE2MAP_IDG_selected_ComboBox.addItem(s);
                }
            }
        }
    }

