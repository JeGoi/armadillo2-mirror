    private void menuFields(String s){    
        if (s.equals(BOWTIE2MAP_M_CM_button.getName())){
            this.BOWTIE2MAP_Options.setSelectedIndex(1);
            this.mappingFields(true);
            this.customMappingFields(true);
            
            if (properties.isSet(BOWTIE2MAP_M_PE_button.getName())) {
                BOWTIE2MAP_M_PE_Panel.setVisible(true);
                peButtonFields(true);
            } else {
                BOWTIE2MAP_M_PE_Panel.setVisible(false);
                peButtonFields(false);
            }
            if (properties.isSet(BOWTIE2MAP_M_IDG_directory_button.getName())) {
                BOWTIE2MAP_M_IDG_main_panel.setVisible(true);
            } else BOWTIE2MAP_M_IDG_main_panel.setVisible(false);
        
        } else {
            this.BOWTIE2MAP_Options.setSelectedIndex(0);
            this.mappingFields(true);
            this.customMappingFields(false);
            
            if (properties.isSet(BOWTIE2MAP_M_PE_button.getName())) {
                this.BOWTIE2MAP_M_PE_Panel.setVisible(true);
                this.peButtonFields(true);
            } else {
                this.BOWTIE2MAP_M_PE_Panel.setVisible(false);
                this.peButtonFields(false);
            }
            if (properties.isSet(BOWTIE2MAP_M_IDG_directory_button.getName())) {
                this.BOWTIE2MAP_M_IDG_main_panel.setVisible(true);
            } else this.BOWTIE2MAP_M_IDG_main_panel.setVisible(false);
        }
    }
    
