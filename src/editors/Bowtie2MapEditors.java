    private void BOWTIE2MAP_IDG_change_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BOWTIE2MAP_IDG_change_buttonActionPerformed
        // TODO add your handling code here:
        JFileChooser d;
        
        if (this.BOWTIE2MAP_IDG_r_text.getText().isEmpty()) {
            d=new JFileChooser(config.getExplorerPath());
        } else {
            d=new JFileChooser(this.BOWTIE2MAP_IDG_r_text.getText());
        }
        
        d.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        d.setAcceptAllFileFilterUsed(false);
        d.setMultiSelectionEnabled(false);
        int result = d.showOpenDialog(this);
        
        if (result==JFileChooser.APPROVE_OPTION) {           
            File dir = d.getSelectedFile();
            
            // Set the text
            String s = dir.getAbsolutePath();
            BOWTIE2MAP_IDG_r_text.setText(s);
            properties.put(BOWTIE2MAP_IDG_r_text.getName(),s);
                
            // Set the combobox
            setIDGCombobox(dir);
        }
    }//GEN-LAST:event_BOWTIE2MAP_IDG_change_buttonActionPerformed
    
